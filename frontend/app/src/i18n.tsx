const storageKey = "secret_dinner_lang";

function normalizeLang(value: string | null | undefined): Language | null {
  if (!value) return null;
  if (value === "en" || value === "ru" || value === "hy") return value;
  return null;
}

function getDefaultLang(): Language {
  const stored = normalizeLang(localStorage.getItem(storageKey));
  if (stored) return stored;

  const browser = (navigator.language || "").toLowerCase();
  if (browser.startsWith("ru")) return "ru";
  if (browser.startsWith("hy")) return "hy";
  return "en";
}

type I18nContextValue = {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: keyof (typeof dictionaries)["en"], vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Language>(() => getDefaultLang());

  const setLang = useCallback((next: Language) => {
    setLangState(next);
    localStorage.setItem(storageKey, next);
  }, []);

  useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  const t = useCallback<I18nContextValue["t"]>(
    (key, vars) => {
      const template = dictionaries[lang][key] ?? dictionaries.en[key] ?? String(key);
      if (!vars) return template;
      return template.replace(/\{(\w+)\}/g, (match, name) => {
        const value = vars[name];
        return value === undefined || value === null ? match : String(value);
      });
    },
    [lang]
  );

  const value = useMemo<I18nContextValue>(() => ({ lang, setLang, t }), [lang, setLang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return ctx;
}
